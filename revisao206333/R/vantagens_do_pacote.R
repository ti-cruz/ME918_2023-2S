vantagens_do_pacote<-function(){
  resposta<-"O benefício de organizar o código em pacotes é a conexão
  entre funções semelhantes expressas no mesmo componente.
  E se tivermos um conjunto de funções e dados que usamos com
  frequência, organizá-los em um pacote torna seu uso mais veloz,
  seguro e limpo. Devemos usar pacotes para organizar o código nos
  casos em que: -Geralmente usamos conjuntos; -Se quisermos
  sintetizar muitas funções relacionadas funcionar em um único
  elemento, não só em termos de cálculos, mas também em termos de
  conceitos. - Distribuir e publicar pacotes de códigos que podem
  ajudar outras pessoas a resolver determinados problemas."
  return(resposta)
}
